public class Loops {
    public static void main(String[] args) {

        System.out.println("For Loop :: ");
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        System.out.println("While Loop :: ");
        int j = 0;
        while (j < 11) {
            System.out.println(j);
            j++;
        }

        System.out.println("Do while Loop :: ");
        int k = 0;
        do {
            System.out.println(k);
            k++;
        } while (k < 11);
    }
}
